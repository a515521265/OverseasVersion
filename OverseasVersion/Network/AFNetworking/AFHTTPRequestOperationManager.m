- (AFHTTPRequestOperation *)HEAD:(NSString *)URLString
                      parameters:(id)parameters
                         success:(void (^)(AFHTTPRequestOperation *operation))success
                         failure:(void (^)(AFHTTPRequestOperation *operation, NSError *error))failure
{
    AFHTTPRequestOperation *operation = [self HTTPRequestOperationWithHTTPMethod:@"HEAD" URLString:URLString parameters:parameters success:^(AFHTTPRequestOperation *requestOperation, __unused id responseObject) {
        if (success) {
            success(requestOperation);
        }
    } failure:failure];

    [self.operationQueue addOperation:operation];

    return operation;
}

- (AFHTTPRequestOperation *)POST:(NSString *)URLString
                      parameters:(id)parameters
                         success:(void (^)(AFHTTPRequestOperation *operation, id responseObject))success
                         failure:(void (^)(AFHTTPRequestOperation *operation, NSError *error))failure
{
    //手机名称
    NSString *iPhoneName = [UIDevice currentDevice].name;
    //app版本号
    NSString *appVerion = [[[NSBundle mainBundle] infoDictionary] objectForKey:@"CFBundleShortVersionString"];
    //位置模型
    NSString *localizedModel = [UIDevice currentDevice].localizedModel;
    //系统名称
    NSString *systemName = [UIDevice currentDevice].systemName;
    //系统版本
    NSString *systemVersion = [UIDevice currentDevice].systemVersion;
    //设备ip地址
    NSString *deviceIP = [self getDeviceIPAddresses];
    

    NSMutableDictionary * dict1 = parameters;

    NSMutableDictionary * dict2 =@{}.mutableCopy;
    [dict2 setObject:iPhoneName forKey:@"mobileName"];
    [dict2 setObject:appVerion forKey:@"appVerion"];
    [dict2 setObject:localizedModel forKey:@"localizedModel"];
    [dict2 setObject:systemName forKey:@"systemName"];
    [dict2 setObject:systemVersion forKey:@"systemVersion"];
    [dict2 setObject:deviceIP forKey:@"deviceIP"];
    
    
    NSMutableDictionary * dict =[NSMutableDictionary new];
    [dict addEntriesFromDictionary:dict1];
    [dict addEntriesFromDictionary:dict2];
    
    
    
    
    AFHTTPRequestOperation *operation = [self HTTPRequestOperationWithHTTPMethod:@"POST" URLString:URLString parameters:dict success:success failure:failure];

    [self.operationQueue addOperation:operation];

    return operation;
}

- (AFHTTPRequestOperation *)POST:(NSString *)URLString
                      parameters:(id)parameters
       constructingBodyWithBlock:(void (^)(id <AFMultipartFormData> formData))block
